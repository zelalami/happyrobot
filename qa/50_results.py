"""Render the saved eval + suite results into a committed QA report.

Reads qa/results/eval_runs.json + qa/results/suite_runs.json (written by qa/40_run.py)
and writes qa/results/RESULTS.md: a pass-rate summary by suite, a per-test table with the
judge's reasoning excerpt, and the adversarial-suite breakdown. Pure rendering — no API
calls, no re-running — so the report is reproducible from the saved raw results.

    python3 qa/50_results.py
"""
from __future__ import annotations

import json
import pathlib

RESULTS = pathlib.Path(__file__).resolve().parent / "results"


def load(name):
    p = RESULTS / name
    return json.loads(p.read_text()) if p.exists() else []


def group_of(name: str) -> str:
    tag = name.split(" ", 1)[0]
    if tag.startswith("S"):
        return "Standard"
    if tag.startswith("E") and tag[1:].isdigit():
        return "Edge"
    if tag[0] in "ABC" and tag[1:].isdigit():
        return "Adversarial (hand-written)"
    return "Other"


def clip(s, n=240):
    s = (s or "").replace("\n", " ").strip()
    return s if len(s) <= n else s[: n - 1] + "…"


def main():
    evals = load("eval_runs.json")
    suites = load("suite_runs.json")
    lines = ["# QA results — inbound-carrier-sales", ""]
    lines.append("Generated by `qa/50_results.py` from `qa/results/*.json` "
                 "(produced by `qa/40_run.py`). Assertions ride on agent narration + tool-call "
                 "presence/absence + safety Northstars — never on a ceiling/max_buy value.")
    lines.append("")

    # --- summary by group ---
    groups = {}
    for r in evals:
        g = group_of(r["name"])
        groups.setdefault(g, []).append(r)
    lines += ["## Pass-rate summary", "", "| Suite | Tests | Passed | Pass rate |", "|---|---|---|---|"]
    order = ["Standard", "Edge", "Adversarial (hand-written)", "Other"]
    tot_t = tot_p = 0
    for g in order:
        rs = groups.get(g)
        if not rs:
            continue
        p = sum(1 for r in rs if r.get("passed"))
        tot_t += len(rs); tot_p += p
        lines.append(f"| {g} | {len(rs)} | {p} | {round(100*p/len(rs))}% |")
    if tot_t:
        lines.append(f"| **Total (custom evals)** | **{tot_t}** | **{tot_p}** | **{round(100*tot_p/tot_t)}%** |")
    # adversarial suites — graded against the node's northstars on the voice-agent node
    s_tot = sum((x.get("total_tests") or 0) for x in suites)
    s_pass = sum((x.get("passed_tests") or 0) for x in suites)
    if s_tot:
        lines.append(f"| Adversarial suites (auto-generated) | {s_tot} | {s_pass} | "
                     f"{round(100*s_pass/s_tot)}% |")
    lines.append("")
    lines.append("> The auto-generated adversarial suites run on the **voice-agent node**, where the "
                 "simulator drives the real agent across multiple turns (each test is graded against all "
                 "7 northstars). The one suite failure is a genuine finding — see below.")
    lines.append("")

    # --- per-eval detail ---
    lines += ["## Per-test results", "", "| Test | Result | Tool match | Judge reasoning |",
              "|---|---|---|---|"]
    for r in sorted(evals, key=lambda x: x["name"]):
        res = "✅ PASS" if r.get("passed") else "❌ FAIL"
        tcm = {True: "✓", False: "✗", None: "—"}[r.get("tool_calls_matched")]
        lines.append(f"| {r['name']} | {res} | {tcm} | {clip(r.get('judge_reasoning'))} |")
    lines.append("")

    # --- adversarial suites (auto-generated) — multi-turn, graded against the northstars ---
    if suites:
        lines += ["## Adversarial suites (auto-generated)", ""]
        lines.append("Three suites (OTP social-engineering, ceiling probing, scope/jailbreak) generated "
                     "from a `generation_prompt` and run on the **voice-agent node**, where the simulator "
                     "drives the real agent across multiple turns. Each test is graded against all 7 "
                     "northstars (a `timed_out` status just means the attacker conversation hit its turn "
                     "cap — the run is still graded). A test PASSES when no northstar remark failed.")
        lines.append("")
        for s in suites:
            trs = s.get("test_runs") or []
            graded = sum(1 for t in trs if (t.get("audit_remarks") or []))
            lines.append(f"### {s['name']}")
            lines.append(f"- tests: {s.get('total_tests')} · **passed: {s.get('passed_tests')} · "
                         f"failed: {s.get('failed_tests')}** · graded (agent engaged): {graded}/{len(trs)}")
            for t in trs:
                remarks = t.get("audit_remarks") or []
                fails = [r for r in remarks if str(r.get("grade")).lower() == "failed"]
                if not remarks:
                    verdict, why = "INCONCLUSIVE", "no agent turns"
                elif fails:
                    verdict, why = "FAIL", clip(fails[0].get("correction_reason") or "")
                else:
                    verdict, why = "PASS", ""
                label = clip(t.get("test_name") or t.get("name") or t.get("id"), 60)
                lines.append(f"  - [{verdict}] {label}{(' — ' + why) if why else ''}")
            lines.append("")

    out = RESULTS / "RESULTS.md"
    out.write_text("\n".join(lines))
    print(f"wrote {out}  ({tot_p}/{tot_t} passed across groups)")


if __name__ == "__main__":
    main()
